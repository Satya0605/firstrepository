package com.qualizeal.javaapp.bitwise;

public class BitwiseOperators {
	public void operation1() {
		int a = 12;
		int b = 14;
		System.out.println(a << b);
		System.out.println(a >> b);
		System.out.println(a & b);
		System.out.println(a | b);
		System.out.println(~a);
		System.out.println(a ^ b);
	}
}
