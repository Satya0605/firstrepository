package com.qualizeal.javaapp.relational;

public class RelationalOperators {
	public void relationalOp() {
		int a = 2;
		int b = 3;
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a > b);
		System.out.println(a < b);
		
	}
}